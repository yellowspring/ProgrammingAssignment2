cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  inv <- x$getinverse()
  if(!is.null(inv)) {
    message("getting inverse matrix")
    return(inv)
  }
  original_matrix <- x$get()
  inv <- solve(original_matrix)
  x$setinverse(inv)
  inv
}
